#include<stdio.h>
#include<unistd.h>

void	ft_putchar(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
	write(1, "\n", 1);
}

int	main (int argc, char **argv)
{
	if (argc > 0)
	{
		ft_putchar(argv[0]);
	}
	return (0);
}
